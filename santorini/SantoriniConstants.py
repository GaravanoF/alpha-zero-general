@njit(cache=True, fastmath=True, nogil=True)
def _decode_action(action):
	# P=2 (utilisation pouvoir), W=2, M=9, B=9 (avec 5 = aucun mouvement/construction)
	power, action_ = divmod(action, 2*9*9)
	worker, action_ = divmod(action_, 9*9)
	move_direction, build_direction = divmod(action_, 9)

	return power, worker, move_direction, build_direction

